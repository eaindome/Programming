/**
 * VARIABLES
 * 1. let - values for let variables can be re-assigned
 * 2. const - all const declarations must be initialized, and once initialized, can't be reassigned
*/
let age = 25;
console.log(`Age: ${age}\n`);

const salary = 1200
console.log(`Salary: ${salary}`);
