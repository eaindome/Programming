public class Main {
    public static void main(String[] args) {
        // other data types
        int myInt = 9;
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello";

        System.out.println("myInt = "+myInt+"\n"+"myFloatNum = "+myFloatNum+"\n"+"myLetter = "+myLetter+"\n"+"myBool = "+myBool+"\n"+"myText = "+myText+"\n");

        // declare Multiple Variables
        int x = 5, y = 6, z = 50;
        System.out.println(x + y + z);

        // one value to multiple variables
        int a, b, c;
        a = b = c = 25;
        System.out.println(a + b + c);

        // identifiers
        int minutesPerHour = 60;
        System.out.println("Using understable identifiers; minutes per hour: " + minutesPerHour);

        // ascii values of characters
        char myVar1 = 65, myVar2 = 66, myVar3 = 67;
        System.out.println(myVar1);
        System.out.println(myVar2);
        System.out.println(myVar3);
        System.out.println();  
    }
}
