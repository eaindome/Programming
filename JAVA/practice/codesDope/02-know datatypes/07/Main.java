/*
 * Write a program to add an integer variable 
 * having value 5 and a double variable having value 6.2.
 */

public class Main {
    public static void main (String[] args) {
        int value = 5;
        double var = 6.2;
        double sum = value + var;
        System.out.println("Sum of 5 and 6.2: "+sum);
    }
}
