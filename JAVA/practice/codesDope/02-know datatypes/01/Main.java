/*
 * Write a Java program to print an 
 * int, a double and a char on screen.
*/

public class Main {
    public static void main(String[] args) {
        int m = 24;
        double n = 0.5;
        char o = 'a';
        System.out.println("Integer: "+ m);
        System.out.println("Double: "+ n);
        System.out.println("Char: "+o);
    }
}
